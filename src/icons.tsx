type IconProps = {
  size?: number;
  className?: string;
  style?: React.CSSProperties;
};

const ico = (path: string, extraProps?: object) =>
  function Icon({ size = 20, className = "", style }: IconProps) {
    return (
      <svg
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill="none"
        stroke="currentColor"
        strokeWidth="1.6"
        strokeLinecap="round"
        strokeLinejoin="round"
        className={className}
        style={style}
        {...extraProps}
      >
        {path.split("|").map((d, i) =>
          d.startsWith("c:") ? (
            <circle key={i} {...Object.fromEntries(d.slice(2).split(",").map((s) => s.split("=")))} />
          ) : d.startsWith("rect:") ? (
            <rect key={i} {...Object.fromEntries(d.slice(5).split(",").map((s) => s.split("=")))} />
          ) : (
            <path key={i} d={d} />
          )
        )}
      </svg>
    );
  };

